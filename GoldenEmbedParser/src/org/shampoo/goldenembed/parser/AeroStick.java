package org.shampoo.goldenembed.parser;

public class AeroStick 
{
    double windSpeed;
    double yaw;
    int windSpeedCounter;
    int yawCounter;
    
    public double getWindSpeed() {
        return windSpeed;
    }
    public void setWindSpeed(double windSpeed) {
        this.windSpeed = windSpeed;
    }
    public double getYaw() {
        return yaw;
    }
    public void setYaw(double yaw) {
        this.yaw = yaw;
    }
    public int getWindSpeedCounter() {
        return windSpeedCounter;
    }
    public void setWindSpeedCounter(int windSpeedCounter) {
        this.windSpeedCounter = windSpeedCounter;
    }
    public int getYawCounter() {
        return yawCounter;
    }
    public void setYawCounter(int yawCounter) {
        this.yawCounter = yawCounter;
    }

}
